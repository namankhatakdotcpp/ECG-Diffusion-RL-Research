"""
mentor_eval/write_summary.py — generate outputs/mentor_review/SUMMARY.md
by inspecting what actually exists on disk (never hardcodes "done").
"""

from __future__ import annotations

import json
from pathlib import Path

import pandas as pd


def _section(title: str, lines: list[str]) -> str:
    return f"## {title}\n\n" + "\n".join(lines) + "\n\n"


def write_summary(cfg) -> Path:
    base = Path(cfg.paths.outputs.results).parent / "mentor_review"
    out_path = base / "SUMMARY.md"

    md = []
    md.append("# Mentor Review — Evaluation Pipeline Summary\n")
    md.append(
        "This folder is generated by `mentor_eval/`, an analysis/visualisation/"
        "evaluation layer on top of the existing PTB-XL data and diffusion "
        "model. It does not retrain the diffusion model. Each section below "
        "tells you what the figure/metric shows, where to find it, and "
        "whether it's a clean comparison of approximated clinical categories "
        "(see the class-mapping caveat) or blocked pending a step that hasn't "
        "happened yet.\n"
    )

    # ── Class mapping caveat (always show — applies to every item) ─────────
    md.append(_section(
        "Class mapping used throughout (read this first)",
        [
            "PTB-XL has no native STEMI / NSTEMI / Coronary-Heart-Disease "
            "labels — only MI-by-anatomical-site codes, ischemia codes, and "
            "rhythm codes. The 4 classes used everywhere below are an "
            "explicit PROXY, not ground truth:",
            "",
            "- **Normal** <- `NORM`",
            "- **STEMI** (proxy) <- acute injury codes (`INJ*`) + MI-by-site "
            "codes (`AMI`, `IMI`, etc.)",
            "- **NSTEMI** (proxy) <- ischemia-without-infarction codes (`ISC*`)",
            "- **AFIB** <- `AFIB`, `AFLT` (direct match)",
            "",
            "\"Coronary Heart Disease\" was dropped as a 5th class — every "
            "PTB-XL code that could represent it already falls inside the "
            "STEMI/NSTEMI proxy codes above, so it would just double-count.",
            "",
            "**Important caveat for figures/metrics involving generated "
            "ECGs**: the diffusion model's own training classes are "
            "NORM/MI/STTC/CD/HYP/OTHER, not these 4. AFIB has no dedicated "
            "trained class (it was merged into OTHER — only 103 records, "
            "below the training threshold), so the model **cannot generate "
            "AFIB**. Anywhere a generated-AFIB result would go, you'll see "
            "an explicit note instead of a substituted OTHER sample.",
            "",
            f"Full mapping table: `mentor_eval/class_mapping.py` "
            f"(`mapping_table_df()`).",
        ],
    ))

    # ── 1. Dataset audit ────────────────────────────────────────────────────
    audit_dir = base / "dataset_audit"
    lines = []
    summary_path = audit_dir / "audit_summary.txt"
    if summary_path.exists():
        lines.append("```\n" + summary_path.read_text().strip() + "\n```")
        lines.append(f"\nFull per-record report: `dataset_audit/audit_report.csv`")
    else:
        lines.append("_Not yet run._ `python -m mentor_eval.dataset_audit`")
    md.append(_section("1. Dataset audit", lines))

    # ── 2. Class mapping table (CSV export) ─────────────────────────────────
    lines = [
        "See the caveat box at the top of this document, and "
        "`mentor_eval/class_mapping.py` for the full code-by-code table — "
        "sanity-check it against `data/ptbxl/scp_statements.csv`.",
    ]
    md.append(_section("2. Disease-class filtering / mapping", lines))

    # ── 3. Lead/class figures ───────────────────────────────────────────────
    lcf_dir = base / "lead_class_figures"
    n_figs = len(list(lcf_dir.glob("lead_*.png"))) if lcf_dir.exists() else 0
    lines = [
        f"{n_figs}/12 lead figures generated." if n_figs else "_Not yet run._",
        "Each figure (`lead_<name>.png`) stacks 4 subplots — Normal / STEMI / "
        "NSTEMI / AFIB — for one lead, single clean trace per class, no "
        "color overlays. These show one representative REAL ECG per class; "
        "look at `lead_V1.png` first (best lead for ST-segment / Q-wave "
        "differences per the mentor's request).",
    ]
    md.append(_section("3. Per-lead, per-class comparison figures", lines))

    # ── 4. Real vs. generated ───────────────────────────────────────────────
    rvg_dir = base / "real_vs_generated"
    lines = []
    if rvg_dir.exists() and any(rvg_dir.rglob("*.png")):
        for cls_dir in sorted(rvg_dir.iterdir()):
            if cls_dir.is_dir():
                n = len(list(cls_dir.glob("*.png")))
                note_path = cls_dir / "GENERATION_NOT_AVAILABLE.txt"
                if note_path.exists():
                    lines.append(f"- **{cls_dir.name}**: {note_path.read_text().strip()}")
                else:
                    lines.append(f"- **{cls_dir.name}**: {n} lead figures (real left, generated right, matched y-scale)")
    else:
        lines.append(
            "_Blocked._ Needs `outputs/models/diffusion_best.pt` "
            "(the trained checkpoint lives on the GPU server, not this "
            "machine). Run `python -m mentor_eval.real_vs_generated` there."
        )
    md.append(_section("4. Original vs. generated comparison", lines))

    # ── 5. Zoomed clinical regions ───────────────────────────────────────────
    zc_dir = base / "zoomed_clinical"
    n_zoom = len(list(zc_dir.glob("*.png"))) if zc_dir.exists() else 0
    lines = [
        f"{n_zoom} zoomed figures generated." if n_zoom else "_Not yet run._",
        "Each figure shows 3 panels (QRS complex / ST segment / T wave) for "
        "one class, boundaries detected by neurokit2's R-peak + wavelet "
        "delineation (not hardcoded offsets) — falls back to literature "
        "offsets only if delineation fails for that beat, and says so in "
        "the figure title.",
    ]
    md.append(_section("5. Zoomed clinical-region plots", lines))

    # ── 6. Training progression ─────────────────────────────────────────────
    tp_dir = base / "training_progression"
    lines = []
    if tp_dir.exists() and any(tp_dir.glob("*.png")):
        for p in sorted(tp_dir.glob("*.png")):
            lines.append(f"- `{p.name}`")
    else:
        lines.append(
            "_Blocked._ Needs at least 2 intermediate checkpoints "
            "(`diffusion_ckpt_ep*.pt`) — none exist on this machine. "
            "Re-run on the GPU server, where step04's `save_every` config "
            "(25 epochs) should have produced several."
        )
    md.append(_section("6. Training progression visualization", lines))

    # ── 7. Loss curves ───────────────────────────────────────────────────────
    lc_dir = base / "loss_curves"
    lines = []
    if (lc_dir / "train_val_loss.png").exists():
        lines.append("`loss_curves/train_val_loss.png` — train loss (per-step, "
                      "light line) and validation loss (per save-interval epoch, "
                      "red line) on one chart.")
    else:
        lines.append(
            "_Blocked._ Needs `logs/diffusion_training_log.csv`, written "
            "during step04 training — doesn't exist on this machine. "
            "(Validation-loss logging already exists in step04, no code "
            "change was needed there — just re-run this script on the GPU server.)"
        )
    md.append(_section("7. Training/validation loss curves", lines))

    # ── 8. Similarity metrics ────────────────────────────────────────────────
    sm_path = base / "similarity_metrics" / "similarity_metrics.csv"
    lines = []
    if sm_path.exists():
        df = pd.read_csv(sm_path)
        lines.append("```\n" + df.to_string(index=False) + "\n```")
        lines.append("\nSee `similarity_metrics/README.md` for how to read each number.")
    else:
        lines.append(
            "_Blocked._ Needs generated samples (checkpoint). "
            "Run `python -m mentor_eval.similarity_metrics` on the GPU server."
        )
    md.append(_section("8. Quantitative similarity metrics", lines))

    # ── 9. Classification validation ─────────────────────────────────────────
    cv_dir = base / "classification_validation"
    lines = []
    real_json = cv_dir / "classifier_real_eval.json"
    gen_json = cv_dir / "classifier_generated_eval.json"
    if real_json.exists():
        m = json.loads(real_json.read_text())
        lines.append(
            f"**Real-data sanity check** (classifier trained + tested on real "
            f"PTB-XL, official test fold): accuracy={m['accuracy']:.3f}, "
            f"macro F1={m['macro_f1']:.3f}, macro AUC={m['macro_auc']:.3f}. "
            f"See `confusion_matrix_real.png`, `roc_curves_real.png`."
        )
    else:
        lines.append("_Not yet run._ `python -m mentor_eval.classification_validation`")

    if gen_json.exists():
        m = json.loads(gen_json.read_text())
        lines.append(
            f"\n**Generated-data evaluation**: accuracy={m['accuracy']:.3f}, "
            f"macro F1={m['macro_f1']:.3f}. Excluded classes: "
            f"{m.get('excluded_classes', [])}. See `confusion_matrix_generated.png`, "
            f"`roc_curves_generated.png`."
        )
    else:
        lines.append(
            "\n_Generated-data stage blocked._ Needs `diffusion_best.pt`. "
            "AFIB is excluded from this stage regardless (no trained AFIB class)."
        )
    md.append(_section("9. Downstream classification validation", lines))

    md.append(_section(
        "How to finish the blocked items",
        [
            "Everything above marked _Blocked_ needs the trained diffusion "
            "checkpoint (and, for item 7, the training log) from the GPU "
            "server. Once training has produced `outputs/models/"
            "diffusion_best.pt` (and ideally several `diffusion_ckpt_ep*.pt` "
            "snapshots, and `logs/diffusion_training_log.csv`), run:",
            "",
            "```bash",
            "python -m mentor_eval.run_all",
            "```",
            "",
            "on that machine to fill in every remaining section.",
        ],
    ))

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(md))
    return out_path


if __name__ == "__main__":
    import sys
    sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
    from utils import load_config
    write_summary(load_config())
