"""
Roadmap/Stage_1_Diagnosis/collect_stage1_results.py — mechanical results
digest for Stage 1, run automatically as the last step of run_stage1.sh.

This does NOT write the narrative reports (baseline_report.md,
dataset_scaling_report.md, directional_conditioning_report.md,
Stage1_Final_Report.md) — interpreting whether a result means "architecture
is the bottleneck" or "AFIB is unreliable" requires judgment a script
shouldn't fake. What this script does is purely mechanical: find whichever
Stage 1 output files exist on disk, pull their headline numbers into one
place, and flag which experiments are still missing outputs — so that
writing the narrative reports afterward is a matter of reading one digest
file instead of hunting through Outputs/ for scattered CSVs.

Writes: Roadmap/Stage_1_Diagnosis/Reports/Stage1_Results_Digest.md
Updates: Roadmap/roadmap.json experiment statuses (best-effort, based on
         which output files are present — not a substitute for reading
         Decisions.md).
"""

from __future__ import annotations

import json
from pathlib import Path

import pandas as pd

REPO_ROOT = Path(__file__).resolve().parents[2]
STAGE1 = REPO_ROOT / "Roadmap" / "Stage_1_Diagnosis"


def _section(title: str) -> str:
    return f"\n## {title}\n\n"


def _df_to_md(df: pd.DataFrame) -> str:
    """Minimal markdown-table renderer (no `tabulate` dependency assumed on
    the GPU server)."""
    cols = list(df.columns)
    header = "| " + " | ".join(str(c) for c in cols) + " |"
    sep = "|" + "|".join("---" for _ in cols) + "|"
    rows = [
        "| " + " | ".join(f"{v:.4f}" if isinstance(v, float) else str(v) for v in row) + " |"
        for row in df.itertuples(index=False)
    ]
    return "\n".join([header, sep, *rows])


def main() -> None:
    lines = ["# Stage 1 Results Digest (auto-generated by collect_stage1_results.py)",
             "",
             "Mechanical summary only — see individual Reports/*.md for the",
             "narrative writeup, and Decisions.md for what each result means.",
             ""]
    found = {}

    # ── Experiment 1: baseline + CFG sweep + sensitivity + routing ──────────────
    lines.append(_section("Experiment 1 — Baseline Reproduction"))
    real_eval = REPO_ROOT / "outputs" / "mentor_review" / "classification_validation" / "classifier_real_eval.json"
    gen_eval = REPO_ROOT / "outputs" / "mentor_review" / "classification_validation" / "classifier_generated_eval.json"
    if real_eval.exists():
        d = json.load(open(real_eval))
        lines.append(f"- Real-data classifier: accuracy={d['accuracy']:.4f}, macro_f1={d['macro_f1']:.4f}, "
                     f"macro_auc={d.get('macro_auc')}\n")
        found["exp1_real"] = True
    else:
        lines.append("- Real-data classifier eval: **not found** — Experiment 1 not yet run.\n")
        found["exp1_real"] = False
    if gen_eval.exists():
        d = json.load(open(gen_eval))
        lines.append(f"- Generated-data classifier: accuracy={d['accuracy']:.4f}, macro_f1={d['macro_f1']:.4f}, "
                     f"excluded_classes={d.get('excluded_classes')}\n")
        found["exp1_gen"] = True
    else:
        lines.append("- Generated-data classifier eval: **not found**.\n")
        found["exp1_gen"] = False

    sens_csv = REPO_ROOT / "outputs" / "conditioning_analysis" / "sensitivity_probe.csv"
    if sens_csv.exists():
        df = pd.read_csv(sens_csv)
        lines.append(f"- Sensitivity probe: {len(df)} timesteps probed, columns: {list(df.columns)}\n")
        found["exp1_sensitivity"] = True
    else:
        lines.append("- Sensitivity probe: **not found**.\n")
        found["exp1_sensitivity"] = False

    ckpt_verif_csv = STAGE1 / "Outputs" / "Experiment_1_Baseline" / "checkpoint_verification.csv"
    if ckpt_verif_csv.exists():
        df = pd.read_csv(ckpt_verif_csv)
        lines.append(_section("Experiment 1.5 — Checkpoint Verification"))
        lines.append(_df_to_md(df) + "\n")
        rising = df["macro_f1"].iloc[-1] >= df["macro_f1"].iloc[max(0, len(df) - 3)]
        lines.append(f"\n- Still improving at final epoch (macro_f1 not yet plateaued in last 3 checkpoints): "
                     f"**{rising}**\n")
        found["exp1_5"] = True
    else:
        lines.append(_section("Experiment 1.5 — Checkpoint Verification"))
        lines.append("**Not found** — run after Experiment 1 (needs per-epoch checkpoints).\n")
        found["exp1_5"] = False

    # ── Experiment 2 (+2.5) ──────────────────────────────────────────────────
    lines.append(_section("Experiment 2 — Dataset Scaling"))
    scaling_csv = STAGE1 / "Outputs" / "Experiment_2_Dataset_Scaling" / "dataset_scaling_metrics.csv"
    if scaling_csv.exists():
        df = pd.read_csv(scaling_csv)
        lines.append(_df_to_md(df) + "\n")
        found["exp2"] = True
    else:
        lines.append("**Not found** — run `run_dataset_scaling.py` on the GPU server.\n")
        found["exp2"] = False

    curve_files = sorted((STAGE1 / "Outputs" / "Experiment_2_Dataset_Scaling").glob("training_curves_size_*.csv")) \
        if (STAGE1 / "Outputs" / "Experiment_2_Dataset_Scaling").exists() else []
    lines.append(_section("Experiment 2.5 — Training Curves"))
    if curve_files:
        lines.append(f"- {len(curve_files)} per-size training curves found: "
                     f"{[f.stem.replace('training_curves_size_', '') for f in curve_files]}\n")
        found["exp2_5"] = True
    else:
        lines.append("**Not found.**\n")
        found["exp2_5"] = False

    # ── Experiment 3 (+3.5) ──────────────────────────────────────────────────
    lines.append(_section("Experiment 3 — Directional Conditioning Analysis"))
    dir_files = sorted((STAGE1 / "Outputs" / "Experiment_3_Directional_Probe").glob("directional_scores_*.csv")) \
        if (STAGE1 / "Outputs" / "Experiment_3_Directional_Probe").exists() else []
    if dir_files:
        for f in dir_files:
            tag = f.stem.replace("directional_scores_", "")
            df = pd.read_csv(f)
            lines.append(f"### tag={tag}\n\n" + _df_to_md(df) + "\n")
        found["exp3"] = True
    else:
        lines.append("**Not found** — run `directional_conditioning_probe.py` on the GPU server.\n")
        found["exp3"] = False

    lines.append(_section("Experiment 3.5 — Layer-wise Direction Probe"))
    layer_files = sorted((STAGE1 / "Outputs" / "Experiment_3_Directional_Probe").glob("layerwise_probe_*.csv")) \
        if (STAGE1 / "Outputs" / "Experiment_3_Directional_Probe").exists() else []
    if layer_files:
        for f in layer_files:
            tag = f.stem.replace("layerwise_probe_", "")
            df = pd.read_csv(f)
            lines.append(f"### tag={tag}\n\n" + _df_to_md(df) + "\n")
        found["exp3_5"] = True
    else:
        lines.append("**Not found.**\n")
        found["exp3_5"] = False

    # ── Experiment 4 (+4.5) ──────────────────────────────────────────────────
    lines.append(_section("Experiment 4 — MentorClassifier Verification"))
    exp4_dir = STAGE1 / "Outputs" / "Experiment_4_Classifier_Verification"
    drift_csv = exp4_dir / "prediction_drift.csv"
    if drift_csv.exists():
        df = pd.read_csv(drift_csv)
        lines.append(_df_to_md(df) + "\n")
        found["exp4"] = True
    else:
        lines.append("**Not found.**\n")
        found["exp4"] = False

    lines.append(_section("Experiment 4.5 — Feature Drift Visualization"))
    drift_feat_csv = exp4_dir / "feature_drift_features.csv"
    if drift_feat_csv.exists():
        df = pd.read_csv(drift_feat_csv)
        has_generated = (df["source"] == "generated").any()
        lines.append(f"- {len(df)} points collected. Includes generated-sample overlay: **{has_generated}**\n")
        found["exp4_5"] = True
    else:
        lines.append("**Not found.**\n")
        found["exp4_5"] = False

    # ── Write digest ─────────────────────────────────────────────────────────
    digest_path = STAGE1 / "Reports" / "Stage1_Results_Digest.md"
    digest_path.parent.mkdir(parents=True, exist_ok=True)
    digest_path.write_text("\n".join(lines))
    print(f"✓ Wrote {digest_path}")

    # ── Best-effort roadmap.json status update ──────────────────────────────
    roadmap_path = REPO_ROOT / "Roadmap" / "roadmap.json"
    if roadmap_path.exists():
        roadmap = json.load(open(roadmap_path))
        exp_status_map = {
            1: found.get("exp1_gen", False),
            2: found.get("exp2", False),
            3: found.get("exp3", False),
            4: found.get("exp4", False),
        }
        for stage in roadmap["stages"]:
            if stage["id"] != "Stage_1_Diagnosis":
                continue
            for exp in stage["experiments"]:
                if exp["id"] in exp_status_map:
                    exp["status"] = "complete" if exp_status_map[exp["id"]] else exp["status"]
        with open(roadmap_path, "w") as f:
            json.dump(roadmap, f, indent=2)
        print(f"✓ Updated {roadmap_path} experiment statuses (best-effort)")

    missing = [k for k, v in found.items() if not v]
    if missing:
        print(f"Still missing: {missing}")
    else:
        print("All Stage 1 experiment outputs present. Ready for Stage1_Final_Report.md.")


if __name__ == "__main__":
    main()
